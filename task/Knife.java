class Knife
{
	int weight;
	double height;
	
	Knife(int weight,double height)
	{
		this.weight=weight;
		this.height=height;
		System.out.println("created the Knife instance variable");
		
	}
	
	public void show(){
		System.out.println("Weight:"+this.weight);
		System.out.println("Height:"+this.height);
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}